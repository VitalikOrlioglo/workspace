package app;

public class Switch {

	public static void main(String[] args) {
		int a = 5;
		switch (a) {
		case 3:
			System.out.println(3);
			break;
		case 4:
			System.out.println(4);
			break;
		case 5:
			System.out.println(5);
			break;
		case 6:
			System.out.println(6);
			break;
		default:
			System.out.println("kein Treffer");;
		}
	}

}
